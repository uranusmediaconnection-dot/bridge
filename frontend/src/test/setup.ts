import "@testing-library/jest-dom";

// Mock scrollIntoView for jsdom (not implemented in jsdom)
Element.prototype.scrollIntoView = vi.fn(() => {});

// Mock IntersectionObserver (not in jsdom)
class MockIntersectionObserver {
  observe() {}
  unobserve() {}
  disconnect() {}
}
